package rem.admin.board.qna.vo;

public class QnaBoardVO {
	private int qna_no;
	private int mem_no;
	private String qna_title;
	private int qna_type;
	private String qna_at;
	private String qna_cont;
	
	
	public int getQna_no() {
		return qna_no;
	}
	public void setQna_no(int qna_no) {
		this.qna_no = qna_no;
	}
	public int getMem_no() {
		return mem_no;
	}
	public void setMem_no(int mem_no) {
		this.mem_no = mem_no;
	}
	public String getQna_title() {
		return qna_title;
	}
	public void setQna_title(String qna_title) {
		this.qna_title = qna_title;
	}
	public int getQna_type() {
		return qna_type;
	}
	public void setQna_type(int qna_type) {
		this.qna_type = qna_type;
	}
	public String getQna_at() {
		return qna_at;
	}
	public void setQna_at(String qna_at) {
		this.qna_at = qna_at;
	}
	public String getQna_cont() {
		return qna_cont;
	}
	public void setQna_cont(String qna_cont) {
		this.qna_cont = qna_cont;
	}
	
	
}
